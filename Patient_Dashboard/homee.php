<div class="facilities-area" id="facilities">

<h1>FACALITIES</h1>

  

	<p><strong>OPD (Allopathy & Homeopathy):</strong>
Patients are registered at the reception and are seen on first come, first serve basis,<img src="northside-hospital-facilities.jpg">  however out of turn consultation may be provided in case of emergency and senior citizen. Patients have the right to consult any doctor. In OPD, clinical consultation is provided to patients which includes history taking, clinical examination, diagnosis and providing prescription to patients besides advising laboratory tests in some cases. OPD has a waiting hall with chairs, a TV, public utilities like drinking water and toilets. Sub waiting areas are available in front of individual consultation rooms. Wheel chairs, trolleys and attendants are there to help very sick patients.<br>

<strong>Dental facility:</strong>
An experienced Dental surgeon provides procedures like Dental Extractions, RCT, Scaling /Cleaning, Fillings, Local curettage.<br>

<strong>Ward/ Indoor facility:</strong>
Ward facilities for observation and management of medical problem like typhoid, acute gastroenteritis, COPD, bronchial asthma, malaria, viral fever, pneumonias etc. There are 3 wards, one special room and one well equipped emergency.<br>

<strong>Minor OT:</strong>
Provides services for minor surgical procedure like dressing of lacerated wound, suturing of minor lacerations & resuturing, excision of corns and sebaceous cysts (done under local anesthesia.<br>

<strong>Physiotherapy:</strong>
Range of physiotherapy services to assist the patients to recover from wide range of musculoskeletal pain-ful disorders. Available modalities are MWD, SWD, U/S, TENS, IFC laser therapy traction unit, magnolia.<br>

<strong>Laboratory services:</strong>
Trained laboratory staff are providing best services which includes painless blood withdrawal Services of one NABL accredited laboratory are also available for carrying out specialised tests, they collect sample from 8 AM to 12 noon. In IIT Hospital.

Sample collection time for IIT laboratory is 8 am to 10.30 am while emergency tests like Blood sugar, platelet count & Hb & blood grouping done in emergent cases throughout OPD hours.<br>

<strong>ECG Services:</strong>
24hours ECG services including machine report, done by trained staff.<br>

<strong>Pharmacy:</strong>
Free reliable quality medicines are available to beneficiaries on doctor prescription during OPD hours. Medicines not available are provided by S.O. signed by prescribing Doctor and Head and collected from on campus chemist shop.<br>

<strong>Radiology/X-ray facility:</strong>
X-Ray pleophos-D, 300 MA Siemens available, X-rays done on all working days during OPD hour. Sonoline G-50 U/S machine, Siemens. Ultrasounds are done once a week.<br>

<strong>Ambulance Services:</strong>
24 hours patient transport vehicle available. Patients are transported from home (on campus) to the hospital and patients referred by emergency duty doctor to empanelled hospital for specialized case.<br></p>
